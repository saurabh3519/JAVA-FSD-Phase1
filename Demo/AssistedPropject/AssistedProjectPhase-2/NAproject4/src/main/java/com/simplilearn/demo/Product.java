package com.simplilearn.demo;

public class Product {
	private int id;
	private String name;
	private String pdis;
	private int pqua;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPdis() {
		return pdis;
	}
	public void setPdis(String pdis) {
		this.pdis = pdis;
	}
	public int getPqua() {
		return pqua;
	}
	public void setPqua(int pqua) {
		this.pqua = pqua;
	}
	
	
	

}
